    public struct Character
    {
        public CP437Character[] Values { get; private set; }

        public bool IsComposite => (Values?.Length ?? 0) > 1;
        public bool IsEmpty => (Values?.Length ?? 0) == 0;

        public static Character Regular(CP437Character value)
        {
            return Composite(value);
        }

        public static Character Composite(params CP437Character[] values)
        {
            return new Character { Values = values };
        }

    }

